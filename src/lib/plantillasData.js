// Normaliza: quita tildes, minúsculas, solo alfanumérico + espacios
export function norm(s) {
  return s.toLowerCase()
    .normalize('NFD').replace(/[\u0300-\u036f]/g, '')
    .replace(/[^a-z0-9 ]/g, '').trim()
}

// Comprueba si el input del usuario coincide con un nombre de jugador
// Acepta nombre, apellido, o cualquier parte relevante (mín 2 chars)
export function matchNombre(input, nombre) {
  const ni = norm(input)
  if (ni.length < 2) return false
  const nn = norm(nombre)
  // Coincidencia exacta o contiene
  if (nn.includes(ni)) return true
  // Coincidencia por parte de palabra (mín 3 chars)
  if (ni.length >= 3) {
    return nn.split(' ').some(p => p.startsWith(ni))
  }
  return false
}

// Calcula bonus de puntos al final de ronda online
// Regla: 1º→N pts, 2º→N-2, 3º→N-3..., último→0
// En empates: todos los empatados reciben el bonus de su posición más alta
export function calcularBonus(jugadoresOrdenados) {
  const n = jugadoresOrdenados.length
  if (n === 0) return []
  const escala = []
  for (let i = 0; i < n; i++) {
    if (i === 0) escala.push(n)
    else if (i === 1) escala.push(Math.max(0, n - 2))
    else escala.push(Math.max(0, n - 2 - (i - 1)))
  }
  const result = new Array(n)
  let i = 0
  while (i < n) {
    const aciertos = jugadoresOrdenados[i].aciertos
    let j = i
    while (j < n && jugadoresOrdenados[j].aciertos === aciertos) j++
    const bonusGrupo = escala[i]
    for (let k = i; k < j; k++) {
      result[k] = { id: jugadoresOrdenados[k].id, bonus: bonusGrupo }
    }
    i = j
  }
  return result
}
